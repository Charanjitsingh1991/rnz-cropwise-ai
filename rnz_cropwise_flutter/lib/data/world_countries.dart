WorldCountry? getCountryByCode(String code) {
  try {
    return worldCountries.firstWhere((country) => country.code == code);
  } catch (e) {
    return null;
  }
}

WorldCountry? getCountryByName(String name) {
  try {
    return worldCountries.firstWhere((country) => country.name == name);
  } catch (e) {
    return null;
  }
}

bool validatePhoneNumber(String phoneNumber, WorldCountry country) {
  final cleanNumber = phoneNumber.replaceAll(RegExp(r'\D'), '');
  return country.phoneLength.any((length) => cleanNumber.length == length);
}

String formatPhoneNumber(String phoneNumber, WorldCountry country) {
  final cleanNumber = phoneNumber.replaceAll(RegExp(r'\D'), '');
  return '${country.phoneCode} $cleanNumber';
}


