// Helper function to get country by code
export const getCountryByCode = (code: string): Country | undefined => {
  return countries.find(country => country.code === code);
};

// Helper function to get country by name
export const getCountryByName = (name: string): Country | undefined => {
  return countries.find(country => country.name === name);
};

// Helper function to validate phone number for a specific country
export const validatePhoneNumber = (phoneNumber: string, country: Country): boolean => {
  const cleanNumber = phoneNumber.replace(/\D/g, '');
  return country.phoneLength.some(length => cleanNumber.length === length);
};

// Helper function to format phone number with country code
export const formatPhoneNumber = (phoneNumber: string, country: Country): string => {
  const cleanNumber = phoneNumber.replace(/\D/g, '');
  return `${country.phoneCode} ${cleanNumber}`;
};
